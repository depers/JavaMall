public class MathExample {

    public static void main(String[] args) {
        // 向上取整
        System.out.println(Math.ceil(0.1));
        System.out.println(Math.ceil(0.5));
        System.out.println(Math.ceil(0.8));

        // 向下取整
        System.out.println(Math.floor(0.1));
        System.out.println(Math.floor(0.5));
        System.out.println(Math.floor(0.8));
    }
}

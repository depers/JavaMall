package cn.bravedawn.basic.type;

public class ByteExample {

    public static void main(String[] args) {
        // 最大
        byte a = 127;

        byte b = 0;

        // 最小
        byte c = -128;

        System.out.println((char) a);
        System.out.println((char) b);
        System.out.println((char) c);

    }
}
